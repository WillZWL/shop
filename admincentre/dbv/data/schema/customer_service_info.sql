CREATE TABLE `customer_service_info` (
  `id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `lang_id` varchar(16) NOT NULL,
  `platform_id` varchar(7) DEFAULT NULL,
  `type` varchar(10) NOT NULL DEFAULT 'WEBSITE',
  `title` varchar(255) DEFAULT NULL COMMENT 'Customer Service Phone Support - Title',
  `content` text COMMENT 'Customer Service Phone Support - Content',
  `short_text` text COMMENT 'text appears in header',
  `long_text` text COMMENT 'first paragraph in contact us page',
  `short_text_status` tinyint(2) unsigned NOT NULL DEFAULT '1' COMMENT '0 = Inactive / 1 = Active',
  `long_text_status` tinyint(2) unsigned NOT NULL DEFAULT '1' COMMENT '0 = Inactive / 1 = Active',
  `operating_hours` text NOT NULL,
  `create_on` timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
  `create_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address, default 127.0.0.1',
  `create_by` varchar(32) NOT NULL DEFAULT 'system',
  `modify_on` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `modify_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address',
  `modify_by` varchar(32) NOT NULL DEFAULT 'system',
  PRIMARY KEY (`id`),
  UNIQUE KEY `lang_id` (`lang_id`,`platform_id`,`type`),
  CONSTRAINT `fk_csi_lang_id` FOREIGN KEY (`lang_id`) REFERENCES `language_copy` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8