CREATE TABLE `supplier` (
  `id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `name` varchar(255) NOT NULL,
  `origin_country` char(2) NOT NULL COMMENT 'International country code (2 characters)',
  `currency_id` char(3) NOT NULL,
  `creditor` tinyint(2) unsigned NOT NULL DEFAULT '0',
  `address_1` varchar(255) DEFAULT NULL,
  `address_2` varchar(255) DEFAULT NULL,
  `address_3` varchar(255) DEFAULT NULL,
  `phone_1` varchar(32) DEFAULT NULL,
  `phone_2` varchar(32) DEFAULT NULL,
  `phone_3` varchar(32) DEFAULT NULL,
  `fax_1` varchar(32) DEFAULT NULL,
  `fax_2` varchar(32) DEFAULT NULL,
  `fax_3` varchar(32) DEFAULT NULL,
  `email` varchar(255) DEFAULT NULL,
  `supplier_reg` bigint(20) unsigned NOT NULL,
  `sourcing_reg` bigint(20) unsigned DEFAULT NULL,
  `note` text,
  `fc_id` varchar(20) DEFAULT NULL,
  `status` tinyint(2) unsigned NOT NULL DEFAULT '1',
  `create_on` timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
  `create_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address, default 127.0.0.1',
  `create_by` varchar(32) NOT NULL DEFAULT 'system',
  `modify_on` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `modify_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address',
  `modify_by` varchar(32) NOT NULL DEFAULT 'system',
  PRIMARY KEY (`id`),
  KEY `idx_s` (`name`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 ROW_FORMAT=COMPACT