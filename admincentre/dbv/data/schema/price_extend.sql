  `sku` varchar(15) NOT NULL,
  `platform_id` varchar(7) NOT NULL,
  `title` varchar(255) DEFAULT NULL,
  `note` varchar(256) DEFAULT NULL COMMENT 'Amazon: Condition note for ixten Product Feed',
  `ext_desc` text,
  `ext_ref_1` bigint(10) unsigned DEFAULT NULL COMMENT 'eBay: Category',
  `ext_ref_2` bigint(10) DEFAULT NULL COMMENT 'eBay: Category2',
  `ext_ref_3` varchar(64) DEFAULT NULL COMMENT 'eBay: StoreCategory',
  `ext_ref_4` varchar(64) DEFAULT NULL COMMENT 'eBay: StoreCategory2',
  `ext_qty` smallint(5) NOT NULL DEFAULT '0',
  `ext_item_id` varchar(32) DEFAULT NULL,
  `ext_condition` varchar(256) DEFAULT NULL COMMENT 'Amazon: Condition for ixten Product Feed',
