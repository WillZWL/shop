CREATE TABLE `display_banner` (
  `id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `display_banner_config_id` bigint(20) unsigned NOT NULL,
  `display_id` bigint(20) unsigned NOT NULL,
  `position_id` tinyint(3) unsigned DEFAULT '1',
  `slide_id` tinyint(3) unsigned DEFAULT '0' COMMENT 'sequence of the slide',
  `country_id` char(2) DEFAULT NULL,
  `lang_id` varchar(16) NOT NULL COMMENT 'e.g. zh-cn, zh-tw, en and etc.',
  `time_interval` tinyint(2) unsigned DEFAULT '0' COMMENT '0 as no rolling',
  `image_id` bigint(20) unsigned DEFAULT NULL,
  `flash_id` bigint(20) unsigned DEFAULT NULL,
  `lytebox_content` text,
  `height` int(3) unsigned NOT NULL,
  `width` int(3) unsigned NOT NULL,
  `usage` varchar(2) DEFAULT 'PV' COMMENT 'PV-Preview, PB- Published',
  `link_type` char(1) DEFAULT 'E' COMMENT 'E - External, I - Internal',
  `link` varchar(256) DEFAULT NULL,
  `priority` tinyint(3) unsigned DEFAULT '9',
  `status` tinyint(2) unsigned NOT NULL DEFAULT '1' COMMENT '0 = Inactive / 1 = Active',
  `create_on` timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
  `create_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address, default 127.0.0.1',
  `create_by` varchar(32) NOT NULL DEFAULT 'system',
  `modify_on` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `modify_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address',
  `modify_by` varchar(32) NOT NULL DEFAULT 'system',
  PRIMARY KEY (`id`),
  UNIQUE KEY `idx_display_banner` (`display_id`,`position_id`,`slide_id`,`country_id`,`lang_id`,`usage`),
  KEY `fk_db_dbcid` (`display_banner_config_id`),
  KEY `fk_db_cid` (`country_id`),
  KEY `fk_db_lid` (`lang_id`),
  KEY `fk_db_iid` (`image_id`),
  KEY `fk_db_fid` (`flash_id`),
  CONSTRAINT `fk_db_cid` FOREIGN KEY (`country_id`) REFERENCES `country_copy` (`id`),
  CONSTRAINT `fk_db_dbcid` FOREIGN KEY (`display_banner_config_id`) REFERENCES `display_banner_config` (`id`),
  CONSTRAINT `fk_db_did` FOREIGN KEY (`display_id`) REFERENCES `display` (`id`),
  CONSTRAINT `fk_db_fid` FOREIGN KEY (`flash_id`) REFERENCES `graphic` (`id`),
  CONSTRAINT `fk_db_iid` FOREIGN KEY (`image_id`) REFERENCES `graphic` (`id`),
  CONSTRAINT `fk_db_lid` FOREIGN KEY (`lang_id`) REFERENCES `language_copy` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8