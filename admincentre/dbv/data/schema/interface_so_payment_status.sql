CREATE TABLE `interface_so_payment_status` (
  `trans_id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `batch_id` bigint(20) NOT NULL,
  `so_no` char(8) DEFAULT NULL,
  `so_trans_id` bigint(20) NOT NULL,
  `payment_gateway_id` varchar(20) DEFAULT NULL,
  `pay_to_account` varchar(64) DEFAULT NULL,
  `card_id` varchar(20) DEFAULT NULL,
  `payment_status` varchar(2) DEFAULT 'N' COMMENT 'N = New / P = Processing / S = Successed / C = Cancelled / F = Failed / B = Chargeback / CF = Cancel Failed',
  `remark` text,
  `mac_token` varchar(64) DEFAULT NULL COMMENT 'MAC / TOKEN Checking',
  `payer_email` varchar(255) DEFAULT NULL COMMENT 'Email address in payment gateway',
  `payer_ref` varchar(64) DEFAULT NULL COMMENT 'PayPal:PAYERID',
  `risk_ref1` varchar(64) DEFAULT NULL COMMENT 'PayPal:PROTECTIONELIGIBILITY / GlobalCollect:AVSRESULT',
  `risk_ref2` varchar(64) DEFAULT NULL COMMENT 'PayPal:PROTECTIONELIGIBILITYTYPE',
  `risk_ref3` varchar(64) DEFAULT NULL COMMENT 'PayPal:ADDRESSSTATUS',
  `risk_ref4` varchar(64) DEFAULT NULL COMMENT 'PayPal:PAYERSTATUS',
  `pay_date` datetime DEFAULT NULL,
  `pending_action` varchar(2) DEFAULT NULL COMMENT 'C = Cancel Payment / R = Reject Challenged / P = Process Challenged / C = Cancel Payment / R = Reject Challenged / CC = Credit Check Pending',
  `batch_status` varchar(2) NOT NULL DEFAULT '' COMMENT 'N = New / R = Ready update to master / S = Success / F = Failed / I = Investigated',
  `failed_reason` text,
  `create_on` timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
  `create_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address, default 127.0.0.1',
  `create_by` varchar(32) NOT NULL DEFAULT 'system',
  `modify_on` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `modify_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address',
  `modify_by` varchar(32) NOT NULL DEFAULT 'system',
  PRIMARY KEY (`trans_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8