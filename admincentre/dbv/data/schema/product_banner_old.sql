CREATE TABLE `product_banner_old` (
  `sku` varchar(15) NOT NULL,
  `display_id` bigint(20) unsigned NOT NULL,
  `position_id` tinyint(3) unsigned NOT NULL DEFAULT '1',
  `lang_id` varchar(16) NOT NULL COMMENT 'e.g. zh-cn, zh-tw, en and etc.',
  `image_id` bigint(20) unsigned DEFAULT NULL,
  `flash_id` bigint(20) unsigned DEFAULT NULL,
  `height` int(3) unsigned NOT NULL,
  `width` int(3) unsigned NOT NULL,
  `link_type` char(1) DEFAULT 'E' COMMENT 'E - External, I - Internal',
  `link` varchar(100) DEFAULT NULL,
  `priority` tinyint(3) unsigned DEFAULT '9',
  `status` tinyint(2) unsigned NOT NULL DEFAULT '1' COMMENT '0 = Inactive / 1 = Active',
  `create_on` timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
  `create_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address, default 127.0.0.1',
  `create_by` varchar(32) NOT NULL DEFAULT 'system',
  `modify_on` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `modify_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address',
  `modify_by` varchar(32) NOT NULL DEFAULT 'system',
  PRIMARY KEY (`sku`,`display_id`,`position_id`,`lang_id`),
  KEY `fk_pb_did` (`display_id`),
  KEY `fk_pb_lid` (`lang_id`),
  KEY `fk_pb_iid` (`image_id`),
  KEY `fk_pb_fid` (`flash_id`),
  CONSTRAINT `fk_pb_did` FOREIGN KEY (`display_id`) REFERENCES `display` (`id`),
  CONSTRAINT `fk_pb_fid` FOREIGN KEY (`flash_id`) REFERENCES `graphic` (`id`),
  CONSTRAINT `fk_pb_iid` FOREIGN KEY (`image_id`) REFERENCES `graphic` (`id`),
  CONSTRAINT `fk_pb_lid` FOREIGN KEY (`lang_id`) REFERENCES `language_copy` (`id`),
  CONSTRAINT `fk_pb_sku_old` FOREIGN KEY (`sku`) REFERENCES `product_copy` (`sku`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8