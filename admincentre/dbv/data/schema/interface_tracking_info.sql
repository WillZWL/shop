CREATE TABLE `interface_tracking_info` (
  `trans_id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `batch_id` bigint(20) unsigned NOT NULL,
  `sh_no` varchar(11) NOT NULL,
  `so_no` varchar(8) NOT NULL,
  `order_number` varchar(8) DEFAULT NULL,
  `status` varchar(15) DEFAULT NULL,
  `tracking_no` varchar(20) DEFAULT NULL,
  `ship_method` varchar(10) DEFAULT NULL,
  `courier_id` varchar(16) DEFAULT NULL,
  `dispatch_date` varchar(19) DEFAULT NULL,
  `weight` double(8,2) unsigned DEFAULT NULL,
  `consignee` varchar(30) DEFAULT NULL,
  `postcode` varchar(16) DEFAULT NULL,
  `country` varchar(2) DEFAULT NULL,
  `amount` double(15,2) unsigned DEFAULT NULL,
  `currency` varchar(3) DEFAULT NULL,
  `charge_out` varchar(9) DEFAULT NULL,
  `qty` smallint(5) unsigned DEFAULT NULL,
  `sku` varchar(19) DEFAULT NULL,
  `qty_shipped` smallint(5) unsigned DEFAULT NULL,
  `shipping_cost` double(15,2) unsigned DEFAULT NULL,
  `batch_status` varchar(2) NOT NULL DEFAULT '' COMMENT 'N = New / R = Ready update to master / S = Success / F = Failed / I = Investigated',
  `failed_reason` varchar(255) DEFAULT NULL,
  `create_on` timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
  `create_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address, default 127.0.0.1',
  `create_by` varchar(32) NOT NULL DEFAULT 'system',
  `modify_on` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `modify_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address',
  `modify_by` varchar(32) NOT NULL DEFAULT 'system',
  PRIMARY KEY (`trans_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 ROW_FORMAT=COMPACT