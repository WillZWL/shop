CREATE TABLE `supplier_cost_history` (
  `id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `prod_sku` varchar(15) NOT NULL,
  `supplier_id` bigint(20) unsigned NOT NULL,
  `currency_id` char(3) NOT NULL,
  `cost` double(15,2) NOT NULL,
  `moq` int(4) DEFAULT NULL,
  `func_currency_id` char(3) NOT NULL DEFAULT 'USD',
  `rate` double(11,6) unsigned NOT NULL DEFAULT '1.000000',
  `sourcing_status` varchar(2) DEFAULT NULL COMMENT 'A = Readily Available / O = Temp of Out Stock / C = Limited Stock / L = Last Lot, D = Discontinued',
  `order_default` tinyint(2) DEFAULT NULL COMMENT '0 = Not Default / 1 = Default',
  `region_default` bigint(20) unsigned DEFAULT NULL,
  `supplier_status` varchar(2) DEFAULT NULL COMMENT 'A = Readily Available / O = Temp Out of Stock',
  `create_on` timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
  `create_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address, default 127.0.0.1',
  `create_by` varchar(32) NOT NULL DEFAULT 'system',
  `modify_on` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `modify_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address',
  `modify_by` varchar(32) NOT NULL DEFAULT 'system',
  PRIMARY KEY (`id`),
  KEY `sch_prod_sku` (`prod_sku`) USING BTREE,
  KEY `sch_create_on` (`create_on`) USING BTREE,
  KEY `sch_supplier_id` (`supplier_id`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 ROW_FORMAT=COMPACT