CREATE TABLE `rma` (
  `id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `so_no` char(32) NOT NULL,
  `client_id` bigint(20) unsigned NOT NULL,
  `forename` varchar(50) NOT NULL,
  `surname` varchar(50) NOT NULL,
  `address_1` text NOT NULL,
  `address_2` text,
  `postcode` varchar(16) DEFAULT NULL,
  `city` varchar(80) NOT NULL,
  `state` varchar(80) DEFAULT NULL,
  `country_id` char(2) NOT NULL,
  `product_returned` varchar(255) NOT NULL,
  `category` tinyint(2) unsigned NOT NULL COMMENT '0 = Machine Only / 1 = Accessory Only / 2 = Both',
  `serial_no` varchar(80) DEFAULT NULL,
  `components` varchar(100) DEFAULT NULL COMMENT 'Machine|AV Cable|Battery|Battery Charger|Case|Remote Control|AC Cable|Scart Adapter|USB Cable|CD-Rom|Manual|AC Adaptor|Box|Other',
  `reason` tinyint(2) unsigned NOT NULL COMMENT '0 = Needs Repair Under Warranty / 1 = Wrong Product Delivered / 2 = Wrong Product Purchased / 3 = Accidently Purchased (conditions apply)',
  `action_request` tinyint(2) unsigned NOT NULL DEFAULT '0' COMMENT '0 = Swap / 1 = Refund / 2 = Repair',
  `details` text,
  `shipfrom` varchar(5) DEFAULT NULL,
  `status` tinyint(3) NOT NULL DEFAULT '0' COMMENT '0 = New',
  `create_on` timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
  `create_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address, default 127.0.0.1',
  `create_by` varchar(32) NOT NULL DEFAULT 'system',
  `modify_on` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `modify_at` int(10) unsigned NOT NULL DEFAULT '2130706433' COMMENT 'IP address',
  `modify_by` varchar(32) NOT NULL DEFAULT 'system',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 ROW_FORMAT=COMPACT