<?php
class SellingPlatformDto
{
    private $id;
    private $selling_platform_id;
    private $type;
    private $name;
    private $description;
    private $status;
    private $country_id;


    public function setId($id)
    {
        $this->id = $id;
    }

    public function getId()
    {
        return $this->id;
    }

    public function setSellingPlatformId($selling_platform_id)
    {
        $this->selling_platform_id = $selling_platform_id;
    }

    public function getSellingPlatformId()
    {
        return $this->selling_platform_id;
    }

    public function setType($type)
    {
        $this->type = $type;
    }

    public function getType()
    {
        return $this->type;
    }

    public function setName($name)
    {
        $this->name = $name;
    }

    public function getName()
    {
        return $this->name;
    }

    public function setDescription($description)
    {
        $this->description = $description;
    }

    public function getDescription()
    {
        return $this->description;
    }

    public function setStatus($status)
    {
        $this->status = $status;
    }

    public function getStatus()
    {
        return $this->status;
    }

    public function setCountryId($country_id)
    {
        $this->country_id = $country_id;
    }

    public function getCountryId()
    {
        return $this->country_id;
    }

}
